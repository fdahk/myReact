/*
 * 实现目标：
 * - 封装一个节流值版 `useThrottle`，把高频变化的输入值压缩成固定时间窗口内最多更新一次。
 * - 适合在 React 场景里处理滚动位置、窗口尺寸、输入联动等高频变化数据。
 * - 进一步理解它为什么属于“自定义 Hook”，以及它和普通 `throttle(fn)` 的本质差异。
 *
 * 核心思路：
 * - 维护一个真正对外暴露的 `throttledValue`，它代表“适合参与渲染的节流后值”。
 * - 每次输入变化时判断距离上次更新时间是否已超过 `delay`。
 * - 若已到窗口边界则立即同步，否则补一个剩余时间的定时器，在窗口结束时更新。
 * - 这个 Hook 的重点不是“拦截某个函数调用”，而是“把高频变化的输入值映射成低频变化的渲染值”。
 *
 * 复杂度 / 运行特征：
 * - 单次判断与调度是 O(1)。
 * - 该实现属于“立即更新 + 尾部补偿”的节流值方案，输出值会比原值有意延迟。
 *
 * 易错点：
 * - `remaining` 的计算容易写反，导致节流窗口失效。
 * - 忘记在 effect 清理旧定时器，会出现尾部多次提交。
 * - 节流值和节流函数并不等价，前者更适合参与渲染，后者更适合包裹事件处理器。
 * - 自定义 Hook 不等于“任何带 use 前缀的函数”，而是“内部调用 React Hook、服务于组件渲染逻辑复用的函数”。
 *
 * 适用场景 / 面试表达点：
 * - 适合讲高频状态治理、节流与防抖区别、Hook 化封装思路。
 * - 面试里可以继续展开 leading/trailing 语义，以及为什么要区分“值节流”和“函数节流”。
 *
 * ------------------------------------------------------------
 * 一、它为什么算 Hook？
 * ------------------------------------------------------------
 *
 * 因为它不是一个普通工具函数，而是一个“基于 React Hook 再封装出来的可复用状态逻辑”。
 *
 * 判断一个函数是不是 React 自定义 Hook，通常看这几点：
 *
 * 1. 名字是否以 `use` 开头
 * 2. 内部是否调用了 React Hook（如 `useState`、`useRef`、`useEffect`）
 * 3. 目的是否是复用“组件渲染相关逻辑”
 *
 * 本文件全部满足：
 *
 * - `useState`：保存节流后的渲染值
 * - `useRef`：保存上一次真正提交的时间戳
 * - `useEffect`：在 `value` 变化后安排“立即同步”或“延后同步”
 *
 * 所以它属于“自定义 Hook”，而不是普通的 JS 节流工具。
 *
 * ------------------------------------------------------------
 * 二、它和普通节流函数有什么区别？
 * ------------------------------------------------------------
 *
 * 最容易混淆的是下面两类东西：
 *
 * 1. 普通节流函数
 *
 *    const throttledFn = throttle(fn, 300)
 *
 *    它的目标是：
 *    “限制某个函数在单位时间内最多执行一次。”
 *
 *    它关心的是“函数调用频率”。
 *    比如：
 *    - scroll 事件处理器
 *    - resize 监听回调
 *    - mousemove 回调
 *
 * 2. `useThrottle(value, 300)`
 *
 *    它的目标是：
 *    “限制某个参与渲染的值，在单位时间内最多更新一次。”
 *
 *    它关心的是“组件里用于渲染的状态/数据更新频率”。
 *
 * 可以用一句话区分：
 *
 * - 普通 `throttle`：节流的是“函数执行”
 * - `useThrottle`：节流的是“组件渲染所消费的值”
 *
 * 再说得更直白一点：
 *
 * - 普通节流函数更偏“事件层 / 行为层”
 * - `useThrottle` 更偏“状态层 / 渲染层”
 *
 * ------------------------------------------------------------
 * 三、为什么普通节流函数不适合直接替代这个 Hook？
 * ------------------------------------------------------------
 *
 * 因为在 React 里，“渲染”不是由你手动调用某个函数决定的，
 * 而是由 state / props 变化驱动的。
 *
 * 普通 `throttle(fn)` 擅长做的是：
 *
 * - 让 `fn` 别被高频触发太多次
 *
 * 但它并不会天然帮你维护：
 *
 * - 当前组件里的节流后 state
 * - 定时器的生命周期
 * - 组件卸载时的清理
 * - 每次 render 后如何拿到最新输入值
 *
 * 而 `useThrottle` 正是把这些 React 运行时问题一起封装了：
 *
 * - 用 `useState` 让“节流后值”真正进入 React 状态系统
 * - 用 `useRef` 跨 render 保存时间戳，而不触发重新渲染
 * - 用 `useEffect` 在依赖变化后安排副作用
 * - 用 effect cleanup 清理旧定时器，避免脏更新
 *
 * 所以它不是“给普通节流函数换个名字”，
 * 而是“把节流能力适配进 React 的 render / commit / effect 模型里”。
 *
 * ------------------------------------------------------------
 * 四、这个 Hook 的底层运行过程是什么？
 * ------------------------------------------------------------
 *
 * 可以按时间顺序理解：
 *
 * 1. 组件首次渲染
 *    - `useState(value)` 先把当前输入值作为初始 `throttledValue`
 *    - 页面先渲染出这个初始值
 *
 * 2. commit 完成后执行 `useEffect`
 *    - 这里才开始检查节流窗口
 *    - 因为副作用逻辑本来就应该发生在提交之后，而不是 render 阶段
 *
 * 3. 如果已经超过节流窗口
 *    - 立即更新 `lastTimeRef.current`
 *    - 调用 `setThrottledValue(value)`
 *    - 触发下一次渲染，让界面拿到新的节流值
 *
 * 4. 如果还没超过节流窗口
 *    - 计算剩余时间 `remaining`
 *    - 注册一个定时器
 *    - 时间到了以后再补一次最新值
 *
 * 5. 如果在定时器触发前，`value` 又变了
 *    - 上一个 effect 的 cleanup 先执行
 *    - 旧定时器被清掉
 *    - 再按新的 `value` 重新计算窗口
 *
 * 这个过程本质上是：
 *
 * - render 阶段只负责声明“我需要一个节流后的值”
 * - effect 阶段负责真正安排定时器与更新时间
 * - state 更新后再驱动新一轮 render
 *
 * ------------------------------------------------------------
 * 五、为什么这里要用 `useRef`，不用普通变量？
 * ------------------------------------------------------------
 *
 * 因为普通变量在函数组件每次重新执行时都会重新初始化。
 *
 * 如果把“上次更新时间”放在普通变量里：
 * - 每次 render 都会丢
 * - 节流窗口就记不住历史时间
 *
 * 而 `useRef` 的好处是：
 * - 能在多次 render 之间保存数据
 * - 改 `ref.current` 不会触发重新渲染
 *
 * 所以时间戳这种“参与逻辑判断，但不直接参与页面展示”的数据，
 * 非常适合放在 `ref` 里。
 *
 * ------------------------------------------------------------
 * 六、为什么这里要用 `useState`？
 * ------------------------------------------------------------
 *
 * 因为 `throttledValue` 是要参与渲染的。
 *
 * React 里，只有 state / props 变化，组件才会重新渲染并把新值体现在 UI 上。
 *
 * 所以这个 Hook 的真正产物不是一个被节流过的回调函数，
 * 而是一个被节流过的“渲染值”。
 *
 * 这也是它和普通 `throttle(fn)` 最大的语义差异之一。
 *
 * ------------------------------------------------------------
 * 七、为什么这里用 `useEffect`，而不是在函数体里直接 setTimeout？
 * ------------------------------------------------------------
 *
 * 因为函数组件函数体属于 render 阶段。
 * render 阶段应该尽量保持纯净，只做“根据输入计算 UI 描述”。
 *
 * 像下面这些动作都属于副作用：
 * - 创建定时器
 * - 清理定时器
 * - 依赖变化后重新调度
 *
 * 所以它们应该放在 `useEffect` 里。
 *
 * 从 React 的运行模型看：
 *
 * - render：计算这次组件长什么样
 * - commit：把结果提交出去
 * - effect：提交后再执行副作用
 *
 * 节流计时器显然属于 effect 阶段的事。
 *
 * ------------------------------------------------------------
 * 八、这个 Hook 更适合什么场景？
 * ------------------------------------------------------------
 *
 * 它更适合这种写法：
 *
 * - 输入框内容实时变化，但联动搜索结果不要每次都刷新
 * - 滚动位置一直在变，但某些派生展示值希望低频更新
 * - 窗口尺寸频繁变化，但图表 / 布局计算希望节流后再更新
 *
 * 也就是说，它特别适合：
 *
 * - “上游输入变化很快”
 * - “下游 UI 渲染或计算不必跟得那么快”
 *
 * 如果你的需求是“把点击事件处理器节流一下”，
 * 往往更适合用普通 `throttle(fn)`，
 * 而不是这个 `useThrottle(value)`。
 */

/*
 * 这里不直接 `import React Hooks from 'react'`，
 * 是因为本仓库里的这批文件主要是“手写题 / 原理题示例”，
 * 并不是一个真实安装了 React 类型声明的独立 demo 工程。
 *
 * 为了避免示例代码在 IDE 中因为缺少 `react` 类型而报错，
 * 这里沿用本专题里其他教学文件的写法，直接声明最小签名。
 */
declare function useState<T>(initialState: T): [T, (value: T) => void];
declare function useRef<T>(initialValue: T): { current: T };
declare function useEffect(effect: () => void | (() => void), deps: readonly unknown[]): void;

export function useThrottle<T>(value: T, delay = 300) {
  // 这是“真正提供给组件渲染使用的值”。
  // `value` 可以高频变化，但 `throttledValue` 会被节流。
  const [throttledValue, setThrottledValue] = useState(value);

  // 保存“上一次真正把值提交给 throttledValue 的时间”。
  // 用 ref 而不是 state，是因为它只参与节流判断，不需要驱动 UI。
  const lastTimeRef = useRef(0);

  useEffect(() => {
    // effect 会在 commit 之后执行。
    // 也就是说，节流调度本身属于副作用，不应该发生在 render 阶段。
    const now = Date.now();
    const remaining = delay - (now - lastTimeRef.current);

    if (remaining <= 0) {
      // 如果已经跨过本次节流窗口，就立即让“节流后值”追上最新输入值。
      //
      // 这一步调用 setState 后，会触发组件重新渲染，
      // 从而让消费这个 Hook 的组件拿到新的 throttledValue。
      lastTimeRef.current = now;
      setThrottledValue(value);
      return;
    }

    const timer = window.setTimeout(() => {
      // 如果还在窗口内，就在窗口结束时补一次最新值。
      // 这保证了：
      // - 更新不会过于频繁
      // - 也不会一直卡在旧值不动
      lastTimeRef.current = Date.now();
      setThrottledValue(value);
    }, remaining);

    // 当 value / delay 改变，或组件卸载时，先清掉旧定时器。
    //
    // 否则会出现两个问题：
    // 1. 旧值对应的定时器晚点触发，污染当前结果
    // 2. 组件都卸载了，定时器还在跑，产生无意义副作用
    return () => window.clearTimeout(timer);
  }, [value, delay]);

  return throttledValue;
}
