/*
 * 实现目标：
 * - 手写一个最基础的防抖函数，让高频触发的操作只在最后一次触发结束后一段时间执行。
 * - 这是搜索输入、窗口 resize、自动保存等场景中最常见的频率控制工具之一。
 *
 * 核心思路：
 * - 通过闭包保存一个定时器句柄。
 * - 每次调用时先清理上一次尚未执行的任务，再重新启动新的延迟执行。
 * - 当用户停止触发达到 `wait` 时间后，才真正执行原函数。
 *
 * 复杂度 / 运行特征：
 * - 单次调用主要是清理和重建定时器，时间复杂度 O(1)。
 * - 高频触发期间原函数不会执行，只有最后一次触发有机会落地。
 *
 * 易错点：
 * - 忘记保存 `this` 和参数时，包装后的函数语义会与原函数不一致。
 * - 防抖会带来“有意延迟”，因此不适合所有需要即时反馈的交互。
 * - 真实项目中常会继续扩展取消、立即执行、立即获取返回值等能力。
 *
 * 适用场景 / 面试表达点：
 * - 适合讲防抖原理、闭包保存定时器、搜索联想与输入优化。
 * - 面试里可以对比节流，并解释为什么防抖更适合“等用户停下来再处理”的业务。
 */

// `debounce` 不是构造函数，也不是类。
// 它本质上是一个高阶函数 / 工厂函数：接收一个函数，返回一个“带防抖能力的新函数”。
// 所以常见调用方式是 `const wrapped = debounce(fn, 300)`，得到的是函数，不是实例对象。
// 虽然普通 function 在语法层面理论上可以被 `new` 调用，但这个函数的设计语义并不是“创建对象”。
function debounce(fn, wait = 300) {
  let timer = null;

  return function debounced(...args) {
    const context = this;
    clearTimeout(timer);
    timer = setTimeout(() => {
      // 保留原始调用上下文与参数，避免包装后行为失真。
      // 为什么用apply？因为apply可以传递多个参数，而call只能传递一个参数
      fn.apply(context, args);
    }, wait);
  };
}

const logInput = debounce((value) => {
  console.log('debounced value:', value);
}, 200);

logInput('a');
logInput('ab');
logInput('abc');
