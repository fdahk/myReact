/*
面试讲解点：数组去重
- 题目本质：本质是保留第一次出现的元素并过滤掉后续重复项，最常见方案是 Set。
- 复杂度：使用 Set 通常时间 O(n)，空间 O(n)。
- 易错点：对象引用去重和基本类型去重语义不同，NaN 边界要注意。
- 追问方向：可以延伸到排序去重、按 key 去重。
- 讲题顺序：先确认需求，再说核心思路，然后写最小实现，最后补边界、优化点和适用场景。
*/

function uniqueArray(list) {
  return [...new Set(list)];
}

console.log(uniqueArray([1, 2, 2, 3, 3, 4]));
