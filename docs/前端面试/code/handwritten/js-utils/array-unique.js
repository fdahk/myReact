/*
 * 实现目标：
 * - 手写一个最简数组去重函数，保留每个元素第一次出现的顺序。
 * - 用来说明现代 JavaScript 中基于 `Set` 的去重思路。
 *
 * 核心思路：
 * - 利用 `Set` 自动去重的特性，把原数组元素依次放入集合。
 * - 再通过展开运算符把集合转换回数组，得到稳定顺序的唯一值列表。
 *
 * 复杂度 / 运行特征：
 * - 在常见实现下时间复杂度约为 O(n)，空间复杂度 O(n)。
 * - `Set` 采用 SameValueZero 语义，因此对 `NaN` 也能正确判重。
 *
 * 易错点：
 * - 对象和数组这类引用类型只会按引用地址判重，不会做深比较。
 * - 若业务要求“按某个字段去重”，应改为 `Map` 或自定义 key 方案。
 * - 老环境面试题里也可能要求手写 `indexOf` / 哈希表版本。
 *
 * 适用场景 / 面试表达点：
 * - 适合讲数组去重、ES6 集合结构、不同判等语义带来的差异。
 * - 面试中可继续扩展排序去重、按 key 去重以及深度去重的取舍。
 */

function uniqueArray(list) {
  // Set 会保留第一次出现顺序，并自动过滤后续重复值。
  return [...new Set(list)];
}

console.log(uniqueArray([1, 2, 2, 3, 3, 4]));
