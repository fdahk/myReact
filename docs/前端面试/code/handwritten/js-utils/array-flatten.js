/*
 * 实现目标：
 * - 手写一个最基础的数组扁平化函数，把任意层级嵌套数组拍平成一维数组。
 * - 用于说明递归遍历在数组结构处理中的经典写法。
 *
 * 核心思路：
 * - 维护一个结果数组作为统一输出容器。
 * - 遍历当前列表时，如果元素还是数组就递归继续展开；否则直接推入结果数组。
 * - 递归结束后即可得到按原顺序展开的一维结果。
 *
 * 复杂度 / 运行特征：
 * - 每个元素通常只访问一次，时间复杂度 O(n)。
 * - 额外空间来自结果数组与递归调用栈，深层嵌套时要注意栈深度。
 *
 * 易错点：
 * - 需要明确是否支持限定展开层数；当前实现是“无限深度完全展开”。
 * - 对非常深的嵌套结构，递归写法可能存在调用栈风险，可替换为显式栈实现。
 * - 要注意保持原有元素顺序不被打乱。
 *
 * 适用场景 / 面试表达点：
 * - 适合讲递归、数组结构处理、`Array.prototype.flat` 的手写原理。
 * - 面试里可以继续扩展深度参数、迭代解法以及尾调用优化讨论。
 */

function flattenArray(input) {
  const result = [];

  function dfs(list) {
    for (const item of list) {
      if (Array.isArray(item)) {
        // 遇到子数组时继续向下展开，直到命中基础值。
        dfs(item);
      } else {
        result.push(item);
      }
    }
  }

  dfs(input);
  return result;
}

console.log(flattenArray([1, [2, [3, 4]], 5]));
