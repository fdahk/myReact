/*
 * 实现目标：
 * - 手写一个基础版柯里化函数，把多参数函数改造成可分批传参的调用方式。
 * - 用最少代码说明“参数收集”与“达到目标参数个数后执行原函数”的核心思想。
 *
 * 核心思路：
 * - 通过递归不断累积已收集参数。
 * - 当参数数量达到原函数声明的 `fn.length` 时，直接执行原函数并返回结果。
 * - 如果参数还不够，就返回一个新函数继续等待后续参数。
 *
 * 复杂度 / 运行特征：
 * - 参数收集过程与参数总数线性相关，整体开销通常为 O(n)。
 * - 该实现更偏演示性质，适合固定参数个数的普通函数。
 *
 * 易错点：
 * - `fn.length` 只统计第一个默认参数之前的形参个数，对默认参数和剩余参数场景要特别说明。
 * - 当前实现没有保留 `this`，若包裹依赖上下文的方法，生产场景通常需要额外处理。
 * - 柯里化与偏函数相关但不完全相同，面试时容易混淆。
 *
 * 适用场景 / 面试表达点：
 * - 适合讲函数式编程、配置复用、延迟执行与高阶函数设计。
 * - 面试里可以继续扩展占位符柯里化、偏函数以及 Lodash 的实现思路。
 */

// ...args ...将剩余参数收集并命名为args
function curry(fn, ...args) {
  if (args.length >= fn.length) {
    return fn(...args);
  }

  // 参数未收满时继续返回函数，递归累积后续传入的参数。
  return (...restArgs) => curry(fn, ...args, ...restArgs);
}

function sum(a, b, c) {
  return a + b + c;
}

console.log(curry(sum)(1)(2)(3));
