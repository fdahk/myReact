/*
实现目标：
- 手写一个最小版 `Webpack loader`，理解 loader 的输入输出模型。
-
核心思路：
- loader 本质上就是一个函数：输入源代码字符串，输出转换后的新代码字符串。
- 可通过 `this.getOptions()` 读取外部传入配置。
- 当前示例把一段 banner 注释拼到源码顶部，演示“源代码转换”最小流程。
-
复杂度 / 运行特征：
- 主要是字符串拼接，时间复杂度与源码长度 n 成正比。
- 真正生产 loader 往往还会配合 source map、异步回调、AST 解析一起工作。
-
易错点：
- loader 运行在构建时，不是浏览器运行时逻辑。
- loader 更偏“文件转换”，plugin 更偏“扩展构建流程能力”。
- 面试里如果继续追问复杂 loader，通常就要讲 AST 而不是纯字符串替换。
-
适用场景 / 面试表达点：
- 适合回答“loader 是什么、最小怎么写”。
- 可继续扩展 markdown 转 html、国际化提取、自动埋点注入等真实场景。
*/

function bannerLoader(source) {
  const options = this.getOptions ? this.getOptions() : {};
  const banner = options.banner || 'generated by custom loader';

  return `/* ${banner} */\n${source}`;
}

module.exports = bannerLoader;
