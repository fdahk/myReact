/**
 * 题目目标：将两个升序数组合并到 `nums1` 中，并保持整体升序。
 * 核心思路：使用三个指针从尾部向前填充。
 * 因为 `nums1` 末尾有足够空位，从后往前比较 `nums1[i]` 和 `nums2[j]`，
 * 可以避免前向合并时覆盖掉 `nums1` 中尚未处理的有效元素。
 * 时间复杂度：O(m + n)，两个数组各遍历一次。
 * 空间复杂度：O(1)，原地完成合并。
 * 易错点 / 面试表达点：
 * 1. 这是数组题，但常和链表“双指针合并”一起考，思路对比很有代表性。
 * 2. 从尾部写入是本题关键，否则会覆盖 `nums1` 还没读到的值。
 * 3. 循环条件只写 `j >= 0` 就够了，因为 `nums2` 必须被全部放进去，`nums1` 剩余部分天然已在正确位置。
 */
function merge(nums1, m, nums2, n) {
  let i = m - 1;
  let j = n - 1;
  let tail = m + n - 1;

  while (j >= 0) {
    if (i >= 0 && nums1[i] > nums2[j]) {
      nums1[tail] = nums1[i];
      i -= 1;
    } else {
      nums1[tail] = nums2[j];
      j -= 1;
    }
    // tail 始终指向当前待填充的最后一个位置。
    tail -= 1;
  }

  return nums1;
}

console.log(merge([1, 2, 3, 0, 0, 0], 3, [2, 5, 6], 3));
